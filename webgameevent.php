        <h3 class="well well-sm"style="max-width:160px;"><strong>Abstract:</strong></h3>

       <p>
            Learning to build a modern web application is daunting. But Ruby on Rails makes it much easier and
            more fun. It includes everything needed to build fantastic applications. Ruby on rails is open source and
            is used to make websites like Basecamp, Github, Shopify, Twitch, Hulu etc.
            Realizing the above need, HASHes programming club in association with Coding ninjas organized a two
            day workshop on web and game development using Ruby on Rails and Django. The agenda of the
            workshop was to enlighten the students with basics of ruby language, create server using Sinatra and
            then implement the gained knowledge to make something productive.
            A day before the workshop students from coding ninja helped the students to install ruby, rails and
            Sinatra. Day 1 began with introduction of ruby as a programming language. A quick glance of datatypes,
            arithmetic operations, conditions, functions, classes and objects in ruby was given. Later use of Sinatra
            was brought into action to dynamically update data on server. After touching briefly on these, day 2 was
            mostly focused on developing a dynamic To-do List. Under the guidance, students made their very first
            application using Ruby on Rails and were asked to add more innovative features to it.

        </p>
